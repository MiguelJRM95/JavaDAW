public class PuntoEspacio{

	
	private int x;
	private int y;
	

	public PuntoEspacio(){

		x = 0;
		y = 0;

	}

	public PuntoEspacio(int x, int y){

		this.x = x;
		this.y = y;

	}


	public int getX(){

		return x;

	}

	public int getY(){

		return y;

	}

	public void setX(){

		this.x = x;

	}

	public void setY(){

		this.y = y;

	}
}